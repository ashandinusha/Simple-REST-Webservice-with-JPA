    private void checkEmpty() {
        String name = txtEditName.getText();
        String userName = txtEditUserName.getText();
        String password = String.valueOf(editOldPasswordField.getPassword());
        String newPassword = String.valueOf(editNewPasswordField.getPassword());
        String cnfPassword = String.valueOf(editConfigPasswordField.getPassword());
        String position = txtEditPosition.getText();
        if (!name.isEmpty() && !userName.isEmpty()
                && !password.isEmpty() && !cnfPassword.isEmpty() && !position.isEmpty() && !newPassword.isEmpty()) {
            btnEditEmployee.setEnabled(true);
        } else {
            btnEditEmployee.setEnabled(false);
        }
    }

    private void getEmployee(int id) {
        try {
            employee.setId(id);
            employee = employeeController.get(employee);
            String name = employee.getName();
            String userName = employee.getUserName();
            String position = employee.getPosition();

            txtEditUserName.setText(userName);
            txtDeleteUserName.setText(userName);
            txtEditName.setText(name);
            txtDeleteName.setText(name);
            txtEditPosition.setText(position);
            txtDeletePosition.setText(position);

        } catch (IOException | RuntimeException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }



    private void btnDeleteEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteEmployeeActionPerformed
        try {
            String deletePassword = String.valueOf(deletePasswordField.getPassword());
            getEmployee(employee.getId());
            if (deletePassword.equals(employee.getPassword())) {
                employeeController.delete(employee);
                JOptionPane.showMessageDialog(null, "Employee Deleted", "Success", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                new EmployeeLoging().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Invalid password", "Error", JOptionPane.ERROR_MESSAGE);

            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);

        } catch (RuntimeException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnDeleteEmployeeActionPerformed


    private void btnEditEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditEmployeeActionPerformed
        String name = txtEditName.getText();
        String userName = txtEditUserName.getText();
        String password = String.valueOf(editOldPasswordField.getPassword());
        String newPassword = String.valueOf(editNewPasswordField.getPassword());
        String cnfPassword = String.valueOf(editConfigPasswordField.getPassword());
        String position = txtEditPosition.getText();
        getEmployee(employee.getId());
        if (password.equals(employee.getPassword())) {
            if (newPassword.equals(cnfPassword)) {
                Employee e = new Employee();
                e.setId(employee.getId());
                e.setName(name);
                e.setUserName(userName);
                e.setPassword(newPassword);
                e.setPosition(position);
                try {
                    employeeController.update(e);
                    JOptionPane.showMessageDialog(null, "Employee Updated", "Success", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                    new EmployeeLoging().setVisible(true);

                } catch (IOException ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);

                } catch (RuntimeException ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Password does not match ", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Invalid password", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditEmployeeActionPerformed


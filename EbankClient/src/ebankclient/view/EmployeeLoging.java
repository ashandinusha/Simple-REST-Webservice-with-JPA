    private void checkEmpty() {
        String name = txtUserName.getText();
        String password = String.valueOf(passwordField.getPassword());
        if (!name.isEmpty() && !password.isEmpty()) {
            btnLogin.setEnabled(true);
        } else {
            btnLogin.setEnabled(false);
        }

    }


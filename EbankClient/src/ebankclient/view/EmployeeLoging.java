    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try {
            String name = txtUserName.getText();
            String password = String.valueOf(passwordField.getPassword());
            Employee employee = new Employee();
            employee.setUserName(name);
            employee.setPassword(password);
            
            EmployeeController controller = new EmployeeController();
            Employee e = controller.login(employee);           
            if(e!=null){
                Object[] options = {"Enter Employee Page",
                    "Enter Customer Page"};
int n = JOptionPane.showOptionDialog(this,
    "Welcome "+name,
    "",
    JOptionPane.YES_NO_OPTION,
    JOptionPane.QUESTION_MESSAGE,
    null,    
    options, 
    options[0]); 
             if(n==0){
                 EmployeeAccountManage view = new EmployeeAccountManage(e.getId());
                 this.dispose();
                 view.setVisible(true);
             }else if(n==1){                  
                CustomerListView view = new CustomerListView();
                this.dispose();
                view.setVisible(true);
             }
                
                
                
                
                
               
            }else{
                 JOptionPane.showMessageDialog(null,"Invalid User name or password", "Error", JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (RuntimeException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnLoginActionPerformed


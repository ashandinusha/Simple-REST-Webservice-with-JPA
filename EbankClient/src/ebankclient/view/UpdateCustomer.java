    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String name = txtCusName.getText();
        DateFormat formatDate = new SimpleDateFormat("yyyy-MM-dd");
        String dob = formatDate.format(cmbDob.getDate());

        String address = txtAddress.getText();
        String telNo = txtTel.getText();
        String email = txtEmail.getText();
        String accNo = txtAccNo.getText();
        int accountType = cmbAccType.getSelectedIndex();
        String sortCode = txtSCode.getText();
        Double balance = Double.parseDouble(txtBalance.getText());
        String cardNo = txtCardNo.getText();

        customer.setName(name);
        customer.setDob(dob);
        customer.setAddress(address);
        customer.setContactNo(telNo);
        customer.setEmail(email);
        customer.setAccountNo(accNo);
        customer.setAccountType(accountType);
        customer.setBalance(BigDecimal.valueOf(balance));
        customer.setSortCode(sortCode);
        customer.setCardNo(cardNo);

        try {
            customerController.update(customer);
            JOptionPane.showMessageDialog(null, "Customer Updated", "Success", JOptionPane.INFORMATION_MESSAGE);
            customerListView.getAllCustomers();
            this.dispose();

        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);

        } catch (RuntimeException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed


    private void setFocus(UpdateCustomer dialog) {
        Component[] focusList;
        focusList = new Component[]{txtCusName, cmbDob, txtAddress, txtTel, txtEmail, cmbAccType, txtAccNo, txtSCode, txtBalance, txtCardNo, btnUpdate};

        dialog.setFocusTraversalPolicy(new FocusTraversalPolicy() {
            int focusNumber = 0;

            @Override
            public Component getLastComponent(Container aContainer) {
                return focusList[focusList.length - 1];
            }

            @Override
            public Component getFirstComponent(Container aContainer) {
                return focusList[0];
            }

            @Override
            public Component getDefaultComponent(Container aContainer) {
                return focusList[1];
            }

            @Override
            public Component getComponentAfter(Container focusCycleRoot,
                    Component aComponent) {
                focusNumber = (focusNumber + 1) % focusList.length;
                if (focusList[focusNumber].isEnabled() == false) {
                    getComponentAfter(focusCycleRoot, focusList[focusNumber]);
                }
                return focusList[focusNumber];
            }

            @Override
            public Component getComponentBefore(Container focusCycleRoot,
                    Component aComponent) {
                focusNumber = (focusList.length + focusNumber - 1)
                        % focusList.length;
                if (focusList[focusNumber].isEnabled() == false) {
                    getComponentBefore(focusCycleRoot, focusList[focusNumber]);
                }
                return focusList[focusNumber];
            }

        });
    }

    private void checkEmpty() {
        String name = txtCusName.getText();
        String address = txtAddress.getText();
        String telNo = txtTel.getText();
        String email = txtEmail.getText();
        String accNo = txtAccNo.getText();
        String sortCode = txtSCode.getText();
        String balance = txtBalance.getText();
        String cardNo = txtCardNo.getText();

        if (!name.isEmpty() && !address.isEmpty()
                && !telNo.isEmpty() && !email.isEmpty() && !accNo.isEmpty()
                && !sortCode.isEmpty() && !balance.isEmpty() && !cardNo.isEmpty()) {
            btnUpdate.setEnabled(true);
        } else {
            btnUpdate.setEnabled(false);
        }

    }

    private void getCustomer(int id) {
        try {
            Customer c = new Customer();
            c.setId(id);
            customer = customerController.get(c);
            String name = customer.getName();

            String dob = customer.getDob();
            java.util.Date date = new SimpleDateFormat("yyyy-MM-dd").parse(dob);
            cmbDob.setDate(date);
            String address = customer.getAddress();
            String telNo = customer.getContactNo();
            String email = customer.getEmail();
            String accNo = customer.getAccountNo();
            int accountType = customer.getAccountType();
            String sortCode = customer.getSortCode();
            String balance = customer.getBalance() + "";
            String cardNo = customer.getCardNo();

            txtCusName.setText(name);
            txtAddress.setText(address);
            txtTel.setText(telNo);
            txtEmail.setText(email);
            txtAccNo.setText(accNo);
            txtSCode.setText(sortCode);
            cmbAccType.setSelectedItem(accountType);
            txtBalance.setText(balance);
            txtCardNo.setText(cardNo);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);

        }
    }

